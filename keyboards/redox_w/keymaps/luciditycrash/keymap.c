void matrix_scan_user(void) {
    uint8_t layer = biton32(layer_state);

    switch (layer) {
        case _QWERTY:
            set_led_off;
            break;
        case _COLEMAKDHMK:
            set_led_off;
            break;
        case _GAMING:
            set_led_off;
        case _SYMB:
            set_led_green;
            clr_led_off;
            break;
        case _NAV:
            set_led_blue;
            clr_led_off;
            break;
        case _ADJUST:
            set_led_red;
            clr_led_off;
            break;
        default:
            break;
    }
};
